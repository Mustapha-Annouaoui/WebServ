# include "../../include/webserv/request.hpp"

// * Constructor ****************************************************************************************************
Request::Request() 
    : location(nullptr),
    keepAlive(true),
    upload_file_fd(-1),
    isChunked(false), 
    contentLength(0), 
    state(CREATING)
{
    // Nothing to do
}
// ******************************************************************************************************************

// * Destructor *****************************************************************************************************
Request::~Request() {
	if (upload_file_fd != -1) {
		close(upload_file_fd);
    }
}
